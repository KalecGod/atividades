    let adicao = n1+n2;
    let subtracao = n1-n2;
    let multiplicacao = n1*n2;
    let divisao = n1/n2;
    let modulo = n1%n2;
    let exponencial = n1**n2;
    let incremento = n1;
    incremento++
    let decremento = n2;
    decremento--
    

